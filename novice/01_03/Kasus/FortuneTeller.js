function tellFortune(Pekerjaan, Lokasi, Pasangan, jumAnak) {
    var future = 'Kamu akan menjadi ' + Pekerjaan + ' di ' + Lokasi + ' dan menikah dengan ' +
    Pasangan + ' ' + ' mempunyai ' + jumAnak + ' anak.';
    document.write(future);
}

tellFortune('Atlit Voli', 'Yogyakarta', 'Adi', 3);
tellFortune('Artis', 'Jakarta', 'Ardhito', 2);
tellFortune('Direktur', 'Jambi', 'Adipati', 1);